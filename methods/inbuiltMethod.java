package methods;

public class inbuiltMethod {
    public static void main(String[]args){
        System.out.println(" This all are the inbuilt method in java ");
        // Math.power is used to solve the power 3 power is 3 = 3*3*3 =27
        System.out.println(Math.pow(3,3));

        // Math.max int is for find maximum value between them.
        System.out.println(Math.max(5 ,8));
        System.out.println(Math.sqrt(9.0)); // square root
        System.out.println(Math.cbrt(8.0)); // cube root
        System.out.println(Math.abs(-20));  // absolute value
        System.out.println(Math.floor(3.14));  // only give value after point 3.0
        System.out.println(Math.min(2,3));
        System.out.println(Math.max(2,Math.max(10,8)));
    }
}
